/// Stable id for a generated course. The code is slugified (spaces / symbols
/// removed) so codes like "CSC 210" produce a clean, URL-safe id.
String generatedCourseId(String departmentId, int semester, String code) {
  final slug = code.toLowerCase().replaceAll(RegExp(r'[^a-z0-9]'), '');
  return '$departmentId-s$semester-$slug';
}

/// Builds the full generated course list for a department from its program.
List<Course> generateCoursesForDepartment(Department dept) {
  final semesters = kCurricula[dept.program];
  if (semesters == null) return const [];
  final courses = <Course>[];
  for (var s = 0; s < semesters.length; s++) {
    for (final spec in semesters[s]) {
      courses.add(Course(
        id: generatedCourseId(dept.id, s + 1, spec.code),
        departmentId: dept.id,
        universityId: dept.universityId,
        name: spec.name,
        code: spec.code,
        semester: s + 1,
      ));
    }
  }
  return courses;
}
